/**
 * Token CSS build: source TS → dist/tokens.css.
 * TS entrypoints (index.ts, uno-theme.ts) are real source files compiled by tsc.
 * Run via `pnpm --filter @chenzy-design/tokens build`.
 */
import { mkdirSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { palette } from './global/color.js';
import * as scales from './global/scales.js';
import { aliasLight, aliasDark } from './alias/index.js';
import { componentTokens } from './components/index.js';
import { tokenValue, type TokenGroup } from './components/token-def.js';
import { buildManifest } from './manifest.js';

const here = dirname(fileURLToPath(import.meta.url));
const dist = resolve(here, '../dist');
mkdirSync(dist, { recursive: true });

const PREFIX = '--cd-';

// 组件 token 值可能是裸字符串或 TokenDef（DSM 元数据）；归一化取 CSS 值。
function vars(group: Record<string, string | { value: string }>, category = ''): string[] {
  const cat = category ? `${category}-` : '';
  return Object.entries(group).map(
    ([k, v]) => `  ${PREFIX}${cat}${k}: ${tokenValue(v)};`,
  );
}

const globalVars = [
  ...vars(palette, 'color'),
  ...vars(scales.spacing, 'spacing'),
  ...vars(scales.radius, 'border-radius'),
  ...vars(scales.fontSize, 'font-size'),
  ...vars(scales.fontWeight, 'font-weight'),
  ...vars(scales.lineHeight, 'line-height'),
  ...vars(scales.shadow, 'shadow'),
  ...vars(scales.motion, 'motion'),
  ...vars(scales.zIndex, 'z'),
  ...vars(scales.breakpoint, 'breakpoint'),
  ...vars(scales.sizing),
];
const aliasVars = vars(aliasLight);
const componentVars = vars(componentTokens);
const darkVars = vars(aliasDark as Record<string, string>);

const css = `/* AUTO-GENERATED by @chenzy-design/tokens build — do not edit */
:root {
  /* --- Global --- */
${globalVars.join('\n')}
  /* --- Alias (semantic) --- */
${aliasVars.join('\n')}
  /* --- Component --- */
${componentVars.join('\n')}
}

[data-theme='dark'] {
${darkVars.join('\n')}
}

/* Base：把页面背景/前景绑到主题 token，使暗色模式开箱即用（对齐 Semi —— Semi 同样
   在 body 上应用主题背景）。消费方若要自管 body 背景，可不引入此规则（见 base 单独导出）
   或用更高优先级覆盖。过渡令亮/暗切换平滑，prefers-reduced-motion 下自动归零。 */
body {
  background-color: var(${PREFIX}color-bg-0);
  color: var(${PREFIX}color-text-0);
  transition:
    background-color var(${PREFIX}motion-duration-mid) var(${PREFIX}motion-ease-standard),
    color var(${PREFIX}motion-duration-mid) var(${PREFIX}motion-ease-standard);
}

@media (prefers-reduced-motion: reduce) {
  :root {
    ${PREFIX}motion-duration-fast: 0ms;
    ${PREFIX}motion-duration-mid: 0ms;
    ${PREFIX}motion-duration-slow: 0ms;
  }
}

/* JS 驱动的全局降级标记：ConfigProvider reducedMotion 显式开启时由 JS 写入，
   优先于系统媒体查询，令依赖 motion-duration token 的动画退化为 0ms。 */
[data-reduced-motion] {
  ${PREFIX}motion-duration-fast: 0ms;
  ${PREFIX}motion-duration-mid: 0ms;
  ${PREFIX}motion-duration-slow: 0ms;
}

/* 屏幕阅读器专用文本工具类（a11y.spec §全局规则）：
   视觉隐藏但屏幕阅读器可读。不可用 display:none / visibility:hidden，
   否则辅助技术也读不到。WCAG 1.4.1 等场景下令颜色/图标非唯一信息载体。 */
.cd-sr-only {
  position: absolute;
  inline-size: 1px;
  block-size: 1px;
  padding: 0;
  margin: -1px;
  overflow: hidden;
  clip: rect(0, 0, 0, 0);
  white-space: nowrap;
  border: 0;
}
`;
writeFileSync(resolve(dist, 'tokens.css'), css);
console.log('[tokens] built dist/tokens.css');

// --- DSM manifest（结构化 token 元数据，供可视化编辑器消费）---
// 把各 group 展开成带前缀的 { '--cd-xxx': value } map（复用与 vars() 相同的前缀规则）。
function toMap(group: Record<string, string>, category = ''): Record<string, string> {
  const cat = category ? `${category}-` : '';
  const out: Record<string, string> = {};
  for (const [k, v] of Object.entries(group)) out[`${PREFIX}${cat}${k}`] = v;
  return out;
}
const globalMap: Record<string, string> = {
  ...toMap(palette, 'color'),
  ...toMap(scales.spacing, 'spacing'),
  ...toMap(scales.radius, 'border-radius'),
  ...toMap(scales.fontSize, 'font-size'),
  ...toMap(scales.fontWeight, 'font-weight'),
  ...toMap(scales.lineHeight, 'line-height'),
  ...toMap(scales.shadow, 'shadow'),
  ...toMap(scales.motion, 'motion'),
  ...toMap(scales.zIndex, 'z'),
  ...toMap(scales.breakpoint, 'breakpoint'),
  ...toMap(scales.sizing),
};
const manifest = buildManifest({
  global: globalMap,
  aliasLight: toMap(aliasLight),
  aliasDark: toMap(aliasDark as Record<string, string>),
  component: componentTokens as TokenGroup,
});
writeFileSync(
  resolve(dist, 'token-manifest.json'),
  JSON.stringify({ generated: '由 @chenzy-design/tokens build 生成', ...manifest }, null, 2),
);
console.log(`[tokens] built dist/token-manifest.json — ${manifest.count} tokens`);
