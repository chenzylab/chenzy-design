<script lang="ts">
  import { Modal, Button } from '@chenzy-design/svelte';

  let visible = $state(false);
  const showDialog = () => {
    visible = true;
  };
  const handleOk = () => {
    visible = false;
  };
  const handleCancel = () => {
    visible = false;
  };
</script>

<Button onclick={showDialog}>点击遮罩层不可关闭</Button>
<Modal
  title="对话框标题"
  {visible}
  onOk={handleOk}
  onCancel={handleCancel}
  maskClosable={false}
>
  <p>This is a modal that cannot be closed by clicking on the mask.</p>
  <p>More content...</p>
</Modal>
